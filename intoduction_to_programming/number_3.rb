# ! NOT
# reverse the given condition
puts !(3 > 4)

# && AND
# Always checks for both true
puts (4 * 4) > 18 && 5 > 3
puts true && true
puts true && false
puts false && true
puts false && false

# || OR
# Always checks for one true
puts true || true
puts true || false
puts false || true
puts false || false
